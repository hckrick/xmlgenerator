    private void jbpreencherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbpreencherActionPerformed

               
  
    try {
        //////inicio do codigo
  
        workbook = Workbook.getWorkbook(new File(jtlocal.getText().trim()));
        
        
    } catch (IOException | BiffException ex) {
        Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
    }
    
                 

                Sheet sheet = workbook.getSheet(0);
                             
                Cell c0 = sheet.getCell(0, 0);
                String titulo = c0.getContents();               
                
               
               jttitulo.setText(titulo);  
               
               
               int linhas = sheet.getRows();
           
               ////inicio do for
            for(int i = 3; i < linhas; i++){

                    Cell ca = sheet.getCell(0, i);
                    Cell cb = sheet.getCell(1, i);
                    Cell cc = sheet.getCell(2, i);
                    
                   
                    
                    String matricula = jttitulo1.getText();
                    String assuntotipo = jttitulo2.getText();
                    String datarequerimento = jttitulo3.getText();
                    
                    String competencia = ca.getContents();
                    String valororiginal = cb.getContents();
                    String valoratualizado = cc.getContents();
               
                        DefaultTableModel mp = (DefaultTableModel) tb1.getModel();                        
                        mp.addRow(new String[]{matricula,assuntotipo, datarequerimento, competencia,valororiginal,valoratualizado});

                    

             }   //////// fim do for

                workbook.close();
             
              
//////fim do codigo



    }//GEN-LAST:event_jbpreencherActionPerformed

